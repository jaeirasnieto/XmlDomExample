
public class Actor {

	private String nombre;
	private String ciudadNatal;
	private Boolean oscar;
	/**
	 * @return the nombre
	 */
	public String getNombre() {
		return nombre;
	}
	/**
	 * @param nombre the nombre to set
	 */
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	/**
	 * @return the ciudadNatal
	 */
	public String getCiudadNatal() {
		return ciudadNatal;
	}
	/**
	 * @param ciudadNatal the ciudadNatal to set
	 */
	public void setCiudadNatal(String ciudadNatal) {
		this.ciudadNatal = ciudadNatal;
	}
	/**
	 * @return the oscar
	 */
	public Boolean getOscar() {
		return oscar;
	}
	/**
	 * @param oscar the oscar to set
	 */
	public void setOscar(Boolean oscar) {
		this.oscar = oscar;
	}
	
}
