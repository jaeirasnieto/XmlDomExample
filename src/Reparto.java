
public class Reparto {

	private String nombre;
	private String personaje;
	private String oscar;
	private String ciudadNatal;
	private OtraPelicula otraPelicula;
	/**
	 * @return the nombre
	 */
	public String getNombre() {
		return nombre;
	}
	/**
	 * @param nombre the nombre to set
	 */
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	/**
	 * @return the personaje
	 */
	public String getPersonaje() {
		return personaje;
	}
	/**
	 * @param personaje the personaje to set
	 */
	public void setPersonaje(String personaje) {
		this.personaje = personaje;
	}
	/**
	 * @return the oscar
	 */
	public String getOscar() {
		return oscar;
	}
	/**
	 * @param oscar the oscar to set
	 */
	public void setOscar(String oscar) {
		this.oscar = oscar;
	}
	/**
	 * @return the ciudadNatal
	 */
	public String getCiudadNatal() {
		return ciudadNatal;
	}
	/**
	 * @param ciudadNatal the ciudadNatal to set
	 */
	public void setCiudadNatal(String ciudadNatal) {
		this.ciudadNatal = ciudadNatal;
	}
	/**
	 * @return the otraPelicula
	 */
	public OtraPelicula getOtraPelicula() {
		return otraPelicula;
	}
	/**
	 * @param otraPelicula the otraPelicula to set
	 */
	public void setOtraPelicula(OtraPelicula otraPelicula) {
		this.otraPelicula = otraPelicula;
	}
	
}
