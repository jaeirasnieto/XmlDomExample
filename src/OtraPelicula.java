
public class OtraPelicula {

	private Short anio;
	private String titulo;
	private String ip;
	private MML mml;
	
	/**
	 * @return the anio
	 */
	public Short getAnio() {
		return anio;
	}
	/**
	 * @param anio the anio to set
	 */
	public void setAnio(Short anio) {
		this.anio = anio;
	}
	/**
	 * @return the titulo
	 */
	public String getTitulo() {
		return titulo;
	}
	/**
	 * @param titulo the titulo to set
	 */
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	/**
	 * @return the ip
	 */
	public String getIp() {
		return ip;
	}
	/**
	 * @param ip the ip to set
	 */
	public void setIp(String ip) {
		this.ip = ip;
	}
	/**
	 * @return the mml
	 */
	public MML getMml() {
		return mml;
	}
	/**
	 * @param mml the mml to set
	 */
	public void setMml(MML mml) {
		this.mml = mml;
	}
	
}
